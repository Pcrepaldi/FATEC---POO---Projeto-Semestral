    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        boolean vazio = false;
        
        ExercicioDAO eDAO = new ExercicioDAO();
        Exercicio ex = new Exercicio();
        
        String nome = jTextFieldNome.getText();
        String parte_corpo = jTextFieldParte_Corpo.getText();
        int qtd_series = Integer.parseInt(jSpinner1.getValue().toString());
        String tempo_intervalo = jTextFieldTempo_Intervalo.getText();
        
        if(nome.length() == 0){
            jTextFieldNome.setBorder(new LineBorder(Color.RED));
            vazio = true;
        }
        
        if(vazio == false){
            
            ex.setCod_treino(0);
            ex.setNomeEx(nome);
            ex.setParte_corpo(parte_corpo);
            ex.setQtd_series(qtd_series);
            ex.setTempo_intervalo(tempo_intervalo);
            
            eDAO.inserirProfessor(ex);
            
            System.out.println("Exercício cadastrado!");
            limpar();
        }else{
            JOptionPane.showMessageDialog(this, "Há campos obrigatórios a serem preenchidos.");
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

