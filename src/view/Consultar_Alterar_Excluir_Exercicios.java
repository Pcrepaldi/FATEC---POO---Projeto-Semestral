    private void jComboConsultaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboConsultaItemStateChanged
        preencher_tabela();
        jTextFieldValor.setText(null);
        
        int valor = jComboConsulta.getSelectedIndex();
        
        switch (valor) {
            case 0:
                jButtonAlterar.setEnabled(true);
                jLabelDigite.setVisible(false);
                jLabelNome.setVisible(false);
                jTextFieldValor.setVisible(false);
                jButtonConsultar.setVisible(false);
                break;
            case 1:
                jButtonAlterar.setEnabled(true);
                jLabelDigite.setVisible(true);
                jLabelDigite.setText("Digite");
                jLabelNome.setText(" o Código:");
                jLabelNome.setVisible(true);
                jTextFieldValor.setVisible(true);
                jButtonConsultar.setVisible(true);
                break;
            case 2:
                jButtonAlterar.setEnabled(true);
                jLabelDigite.setVisible(true);
                jLabelDigite.setText("Digite");
                jLabelNome.setText(" o Nome:");
                jLabelNome.setVisible(true);
                jTextFieldValor.setVisible(true);
                jButtonConsultar.setVisible(true);
                break;
            case 3:
                jButtonAlterar.setEnabled(true);
                jLabelDigite.setVisible(true);
                jLabelDigite.setText("");
                jLabelNome.setText("Parte do Corpo:");
                jLabelNome.setVisible(true);
                jTextFieldValor.setVisible(true);
                jButtonConsultar.setVisible(true);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_jComboConsultaItemStateChanged

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        Exercicio exercicio = new Exercicio();
        
        exercicio.setCod_exerc(Integer.parseInt(jTextFieldCod_Exercicio.getText()));
        exercicio.setNome(jTextFieldNome.getText());
        exercicio.setParte_corpo(jTextFieldParte_Corpo.getText());
        exercicio.setQtd_series(Integer.parseInt(jSpinnerQtd_Series.getValue().toString()));
        exercicio.setTempo_intervalo(jTextFieldTempo_Intervalo.getText());
        
        ExercicioDAO eDAO = new ExercicioDAO();
        eDAO.alterarExercicio(exercicio);

        preencher_tabela();
        limpar();
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        if(jComboConsulta.getSelectedIndex() == 1){
            if(jTextFieldValor.getText().length() == 0){
                jTextFieldValor.setBorder(new LineBorder(Color.RED, 1));
                JOptionPane.showMessageDialog(null, "Digite um valor para consultar");
            }else{
                preencher_tabela();
            } 
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

