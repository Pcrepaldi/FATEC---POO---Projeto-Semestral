    public void preencher_tabela(){
        modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        ExercicioDAO eDAO = new ExercicioDAO();

        if(jComboConsulta.getSelectedIndex() == 0){
            ArrayList<Exercicio> lista = eDAO.consultarExercicio();
            for (int i = 0; i < lista.size(); i++) {        
                modelo.addRow(new Object[]{
                    lista.get(i).getCod_exerc(),    
                    lista.get(i).getNomeEx(),    
                    lista.get(i).getParte_corpo(),
                    lista.get(i).getQtd_series(),
                    lista.get(i).getTempo_intervalo(),
                    });
            }
        }else if(jComboConsulta.getSelectedIndex() == 1){
            ArrayList<Exercicio> lista = eDAO.consultarExercicioCodigo("cod_exercicio", jTextFieldValor.getText());
            for (int i = 0; i < lista.size(); i++) {        
                modelo.addRow(new Object[]{
                    lista.get(i).getCod_exerc(),    
                    lista.get(i).getNomeEx(),    
                    lista.get(i).getParte_corpo(),
                    lista.get(i).getQtd_series(),
                    lista.get(i).getTempo_intervalo(),
                    });
            }
                
        }else if(jComboConsulta.getSelectedIndex() == 2){
            ArrayList<Exercicio> lista = eDAO.consultarExercicioPersonalizada("nome", jTextFieldValor.getText());
            for (int i = 0; i < lista.size(); i++) {        
                modelo.addRow(new Object[]{
                    lista.get(i).getCod_exerc(),    
                    lista.get(i).getNomeEx(),    
                    lista.get(i).getParte_corpo(),
                    lista.get(i).getQtd_series(),
                    lista.get(i).getTempo_intervalo(),
                    });
            }
        
        }else if(jComboConsulta.getSelectedIndex() == 3){
            ArrayList<Exercicio> lista = eDAO.consultarExercicioPersonalizada("parte_corpo", jTextFieldValor.getText());
            for (int i = 0; i < lista.size(); i++) {        
                modelo.addRow(new Object[]{
                    lista.get(i).getCod_exerc(),    
                    lista.get(i).getNomeEx(),    
                    lista.get(i).getParte_corpo(),
                    lista.get(i).getQtd_series(),
                    lista.get(i).getTempo_intervalo(),
                    });
            }
        
        }
 }
    
