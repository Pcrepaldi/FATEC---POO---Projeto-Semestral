    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        Exercicio exercicio = new Exercicio();
        
        exercicio.setCod_exerc(Integer.parseInt(jTextFieldCod_Exercicio.getText()));
        
        ExercicioDAO eDAO = new ExercicioDAO();
        eDAO.excluirExercicio(jTextFieldCod_Exercicio.getText());
        
        preencher_tabela();
        limpar();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int row = jTable1.getSelectedRow();

        String cod_exercicio = String.valueOf(modelo.getValueAt(row, 0));
        String nome = String.valueOf(modelo.getValueAt(row, 1));
        String parte_corpo = String.valueOf(modelo.getValueAt(row, 2));
        Integer series = Integer.parseInt(modelo.getValueAt(row, 3).toString());
        String intervalo = String.valueOf(modelo.getValueAt(row, 4));

        jTextFieldCod_Exercicio.setText(cod_exercicio);
        jTextFieldNome.setText(nome);
        jTextFieldParte_Corpo.setText(parte_corpo);
        jSpinnerQtd_Series.setValue(series);
        jTextFieldTempo_Intervalo.setText(intervalo);
        
    }//GEN-LAST:event_jTable1MouseClicked

